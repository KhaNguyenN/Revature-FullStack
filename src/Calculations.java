
public class Calculations {

	public static double cupToTeaSpoons(double conversionUnit) {
		double teaSpoon = conversionUnit * 48; 
		return teaSpoon;
	}
	
	public static double milesToKilo(double conversionUnit) {
		double kilo = conversionUnit * 1.60934;
		return conversionUnit;
	}
	
	public static double usGallonsToImperialGallons(double conversionUnit) {
		double impGallons = conversionUnit / 1.201;
		return impGallons;
	}


}
